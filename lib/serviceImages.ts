// Priority: serviceImageMap by name > service DB image > category image > default
export function getServiceImage(
  name: string,
  categoryImage?: string,
  serviceDbImage?: string
): string {
  const key = name.toLowerCase().trim();

  // 1. Exact match in our curated map
  if (serviceImageMap[key]) return serviceImageMap[key];

  // 2. Try stripping common suffixes
  const baseKey = key.replace(/\s*(service|repair|installation|assembly|fitting)\s*$/, "").trim();
  if (serviceImageMap[baseKey]) return serviceImageMap[baseKey];

  // 3. Try first word partial match (e.g. "fan" matches "fan installation")
  const firstWord = key.split(" ")[0];
  const partialMatch = Object.keys(serviceImageMap).find(k => k.startsWith(firstWord));
  if (partialMatch) return serviceImageMap[partialMatch];

  // 4. Use DB image if it's a valid URL
  if (serviceDbImage && serviceDbImage.startsWith("http")) return serviceDbImage;

  // 5. Category fallback
  if (categoryImage && categoryImage.startsWith("http")) return categoryImage;

  // 6. Generic default
  return "https://images.unsplash.com/photo-1581092918056-0c4c3acd3789?w=500&fit=crop&q=80";
}
